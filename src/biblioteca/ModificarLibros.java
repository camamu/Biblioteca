    private void modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarActionPerformed
        try {
            String campo = (String) campoMod.getSelectedItem();
            String titulo = (String) libroMod.getSelectedItem();
            String updateSQL = "UPDATE Libros SET "+campo+" = ? WHERE id = ?";
            String querySQL = "SELECT id FROM Libros WHERE Titulo = ?";
            
            Class.forName("com.mysql.jdbc.Driver");
            conexion = DriverManager.getConnection("jdbc:mysql://localhost/Biblioteca", "root", "");
            PreparedStatement ps = conexion.prepareStatement(updateSQL);
            PreparedStatement ps2 = conexion.prepareStatement(querySQL);
            
            ps2.setString(1, titulo);
            ResultSet rs = ps2.executeQuery();
            
            ps.setString(1, datosNuevos.getText());
            int id = 0;
            while (rs.next()) {
               id = rs.getInt(1);
                 
            }
            ps.setInt(2, id);
           ps.executeUpdate();
           
           Statement st = conexion.createStatement();
            ResultSet rs2 = st.executeQuery("SELECT * FROM Libros");
            libroMod.removeAllItems();
            jTextArea1.setText("");
            while (rs2.next()) {
                libroMod.addItem(rs2.getString(2));
                jTextArea1.append("Id: " + rs2.getInt(1) + "\n" + "Titulo: " + rs2.getString(2) + "\n" + "Numero ejemplares: " + rs2.getInt(3) + "\n" + "Editorial: " + rs2.getString(4) + "\n" + "Numero paginas: "
                        + rs2.getInt(5) + "\n" + "Id: " + rs2.getDate(6) + "\n" + "------------------------- \n");
            }
            
            
        } catch (SQLException|ClassNotFoundException ex) {
            Logger.getLogger(ModificarLibros.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_modificarActionPerformed

