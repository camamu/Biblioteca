    private void busNumLibrosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_busNumLibrosActionPerformed
        String sSocio = (String) socio.getSelectedItem();
        String querysSQL = "SELECT id FROM Socios WHERE Nombre = ? AND Apellidos LIKE ?";
        String nombre[];
        nombre = sSocio.split(" ");
        String nombre2 = nombre[0];
        String apellidos = nombre[1];
        int idSocio = 0;
        try {

            Class.forName("com.mysql.jdbc.Driver");
            conexion = DriverManager.getConnection("jdbc:mysql://localhost/Biblioteca", "root", "");
            Statement st = conexion.createStatement();
            //busca el id del usuario seleccionado
            PreparedStatement ps2 = conexion.prepareStatement(querysSQL);
            ps2.setString(1, nombre2);
            ps2.setString(2, apellidos + "%");
            ResultSet rs2 = ps2.executeQuery();
            while (rs2.next()) {
                idSocio = rs2.getInt(1);
            }

            // crea la queery para saber el numero de libros
            ResultSet rs = st.executeQuery("SELECT COUNT(id_libro) FROM prestamos WHERE id_socio = " + idSocio + ";");

            //ejecutamos la query del rs
            while (rs.next()) {
                jTextArea1.setText("Numero de libros prestados a " + sSocio + " es de: " + rs.getInt(1));
            }

        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(ConsultaPres.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_busNumLibrosActionPerformed

